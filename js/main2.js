var GuineaPig = Class.create(Sprite, {
    initialize: function(x, y) {
        Sprite.call(this, 32, 32);
        this.image = game.assets['assets/player.gif'];
        this.x = x;
        this.y = y;
        this.opacity = 1;//透明度
        this.xx = 2;//Velocidad en x
        this.yy = 2;
        this.frame = 0;
        this.dir = 0;

    },
    onenterframe: function() {
        if(this.dir == RIGHT && map.hitTest(this.x + 16, this.y + 32)){
            this.xx = -this.xx;
            this.dir = LEFT;
            this.frame= 0;
        }else if(this.dir == LEFT && map.hitTest(this.x, this.y + 32)){
            this.xx = this.xx;
            this.dir = RIGHT;
            this.frame = 0;
        }
        if(!map.hitTest(this.x, this.y + 32))
            this.y += this.yy;
        if(game.input.left){
            this.x -= this.xx;
        }
        if(game.input.right){
            this.x += this.xx;
        }
    }
});

