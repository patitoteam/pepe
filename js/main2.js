var GuineaPig = Class.create(Sprite, {
    initialize: function(x, y) {
        Sprite.call(this, 32, 32);
        this.image = game.assets['assets/player.gif'];
        this.x = x;
        this.y = y;
        this.opacity = 1;
        this.xx = 9;//Velocidad en x
        this.yy = 9.81;
        this.frame = 0;
        this.dir = 0;
    },
    onenterframe: function() {
        // Avanzar

        if (game.input.right) {
            this.x += this.xx;
            if (game.frame%2==0) {
                this.frame++;
                this.frame%=4;
            }
        } else if (game.input.left) {
            this.x -= this.xx ;
        }

        if(!map.hitTest(this.x+16, this.y + 32) && !gameOver) {
          this.y += this.yy;
        }
        if (map.hitTest(this.x+16, this.y + 32)) {
          console.log('colision');
        }
        if (this.y >= Game.instance.height - 32 && !gameOver) {
            gameOver = true;
            alert('game over');
        }

    }
});

